package com.JH.io;


public class IoDTO {
	private int num;
	private int ionum;
	private int in_mon;
	private int out_mon;
	private String  iodate;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getIonum() {
		return ionum;
	}
	public void setIonum(int ionum) {
		this.ionum = ionum;
	}
	public int getIn_mon() {
		return in_mon;
	}
	public void setIn_mon(int in_mon) {
		this.in_mon = in_mon;
	}
	public int getOut_mon() {
		return out_mon;
	}
	public void setOut_mon(int out_mon) {
		this.out_mon = out_mon;
	}
	public String getIodate() {
		return iodate;
	}
	public void setIodate(String iodate) {
		this.iodate = iodate;
	}
}
